class CreditsTvShows {
  List<CreditsTvShowsCast>? cast;
  List<CreditsTvShowsCrew>? crew;
  int? id;

  CreditsTvShows({this.cast, this.crew, this.id});

  CreditsTvShows.fromJson(Map<String, dynamic> json) {
    if (json['cast'] != null) {
      cast = <CreditsTvShowsCast>[];
      json['cast'].forEach((v) {
        cast!.add(CreditsTvShowsCast.fromJson(v));
      });
    }
    if (json['crew'] != null) {
      crew = <CreditsTvShowsCrew>[];
      json['crew'].forEach((v) {
        crew!.add(CreditsTvShowsCrew.fromJson(v));
      });
    }
    id = json['id'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = <String, dynamic>{};
    if (cast != null) {
      data['cast'] = cast!.map((v) => v.toJson()).toList();
    }
    if (crew != null) {
      data['crew'] = crew!.map((v) => v.toJson()).toList();
    }
    data['id'] = id;
    return data;
  }
}

